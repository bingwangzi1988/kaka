package com.common.adapter;

public abstract class Adapter {
	public abstract Object doComm(Object obj)throws Exception;

	public String ip;
	public int port;
	public int timeout;

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}

	public int getTimeout() {
		return timeout;
	}

	public void setTimeout(int timeout) {
		this.timeout = timeout;
	}

}
